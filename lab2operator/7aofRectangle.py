# Q3. Calculate the area of rectangle.

"""
The area of rectangle can be calculated using formula:
     Area of Rectangle = Length * Width
"""

# Here I used input() as float(), which means it can take input as float and integer both.
length = float(input("Enter Length: "))
width = float(input("Enter Width: "))

areaOfRectangle = length * width
print("Area of Rectangle is", areaOfRectangle)