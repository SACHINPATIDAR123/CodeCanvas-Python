# Q4. Calculate the area of Square.

"""
The area of square can be calculated using formula:
     Area of Square = Side ** 2
"""

# Here I used input() with float(), which means it can take input as float and integer both.
sideOfSquare = float(input("Enter Side of the Square:"))

areaOfSquare = sideOfSquare ** 2
print("Area of Square is", areaOfSquare)