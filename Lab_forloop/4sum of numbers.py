#Question.4 Calculate the sum of numbers from 1 to 10

#here we initiated the sum variable to 0
total = 0

#here Iterate over the numbers from 1 to 10

for number in range(1, 11):
    
# Add each number to the total
    total = total + number

# Print the sum of numbers from 1 to 10
print("The sum of numbers from 1 to 10 is:", total)
